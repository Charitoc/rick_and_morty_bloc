class Character {
  final String name;
  final String status;
  final String species;
  final String type;
  final String gender;
  final String location;
  final String image;
  final String firstEpisode;
  Character({
    required this.name,
    required this.status,
    required this.species,
    required this.type,
    required this.gender,
    required this.location,
    required this.image,
    required this.firstEpisode,
  });
}
